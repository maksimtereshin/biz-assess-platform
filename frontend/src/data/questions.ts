// Обновляем количество вопросов в категориях и подкатегориях
function updateQuestionCounts() {
  // Сбрасываем счетчики
  Object.values(categories).forEach(category => {
    category.questionCount = 0;
    category.subcategories.forEach(sub => sub.questionCount = 0);
  });

  // Подсчитываем вопросы для экспресс версии
  expressQuestions.forEach(question => {
    const category = categories[question.category];
    if (category) {
      category.questionCount++;
      const subcategory = category.subcategories.find(sub => sub.name === question.subcategory);
      if (subcategory) {
        subcategory.questionCount++;
      }
    }
  });
}

updateQuestionCounts();

